/**
 * Ошибка плагина шифрования.
 *
 * Возникает только на неверном использовании: неизвестное имя шифра, шифрование запроса,
 * в котором нет текста. Сам алгоритм не падает — непонятную строку он просто не считает
 * зашифрованной.
 */
export class CryptError extends Error {
  constructor(message: string) {
    super(message);
    this.name = 'CryptError';
  }
}
